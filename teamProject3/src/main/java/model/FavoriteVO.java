package model;

public class FavoriteVO {
	private String favorite_code;
	private int favorite_no;
	private String member_id;
	public String getFavorite_code() {
		return favorite_code;
	}
	public void setFavorite_code(String favorite_code) {
		this.favorite_code = favorite_code;
	}
	public int getFavorite_no() {
		return favorite_no;
	}
	public void setFavorite_no(int favorite_no) {
		this.favorite_no = favorite_no;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}

	
	
}
